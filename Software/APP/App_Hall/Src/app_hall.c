static void App_Hall_UpdateAngleSensor0(void)
{
    uint8_t lut_idx;

    /* Sensor 0 */
    lut_idx = APP_HALL_VALIDATE_IDX(Hal_Hall_Results[APP_HALL_SENSOR_0].angle >> APP_HALL_ANGLE_DIV_4);
    App_Hall_Angle[APP_HALL_SENSOR_0] = App_Hall_ValidateAngle((int16_t)Hal_Hall_Results[APP_HALL_SENSOR_0].angle - App_Hall_AngleCorrectionLUT[lut_idx]);
}

static int16_t App_Hall_ValidateAngle(int16_t ang)
{
    int16_t ret_ang = 0;

    if(ang < APP_HALL_0_DEG)
    {
        ret_ang = ang + APP_HALL_360_DEG;
    }
    else if(ang > APP_HALL_360_DEG)
    {
        ret_ang = ang - APP_HALL_360_DEG;
    }
    else
    {
        ret_ang = ang;
    }

    return ret_ang;
}